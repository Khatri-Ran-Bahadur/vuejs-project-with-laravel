<template>
    <div class="container">
        <div class="row mt-5" >
          <div class="col-md-12">
            <div class="card" v-if="$gate.isAdmin()">
              <div class="card-header">
                <h3 class="card-title">Users Table</h3>

                <div class="card-tools">
                    <button class="btn btn-success" @click="newModal">Add New <i class="fa fa-user-plus white"></i></button>
                </div>
              </div>
              <!-- /.card-header -->
              <div class="card-body table-responsive p-0">
                <table class="table table-hover">
                  <thead>
                    <tr>
                      <th>ID</th>
                      <th>Name</th>
                      <th>Email</th>
                      <th>Type</th>
                      <th>Registered At</th>
                      <th>Modify</th>
                    </tr>
                  </thead>
                  <tbody>
                    <tr v-for="user in users.data" :key="user.id">
                      <td>{{user.id}}</td>
                      <td>{{user.name}}</td>
                      <td>{{user.email}}</td>
                      <td>{{user.type| upText}}</td>
                      <td>{{user.created_at | myDate}}</td>
                      <td>
                          <a href="#" @click="editModal(user)">
                              <i class="fa fa-edit blue"></i>
                          </a>
                          /
                          <a href="#" @click="deleteUser(user.id)">
                              <i class="fa fa-trash red"></i>
                          </a>
                      </td>
                    </tr>
                    
                  </tbody>
                </table>
              </div>
              <!-- /.card-body -->
              <div class="card-footer">
                <pagination :data="users" @pagination-change-page="getResults"></pagination>
              </div>
            </div>
            <!-- /.card -->
          </div>
        </div>

        <div v-if="!$gate.isAdmin()">
          <not-found></not-found>
        </div>

        <div class="modal fade" id="addNew" tabindex="-1" role="dialog" aria-labelledby="exampleModalLongTitle" aria-hidden="true">
          <div class="modal-dialog modal-dialog-centered" role="document">
            <div class="modal-content">
              <div class="modal-header">
                <h5 v-show="!editmode" class="modal-title" id="exampleModalLongTitle">Add New</h5>
                <h5 v-show="editmode" class="modal-title" id="exampleModalLongTitle">Update User's Info</h5>
                <button type="button" class="close" data-dismiss="modal" aria-label="Close">
                  <span aria-hidden="true">&times;</span>
                </button>
              </div>
               <form @submit.prevent="editmode?updateUser():createUser()">
                  <div class="modal-body">
                    
                    <div class="form-group">
                      <input v-model="form.name" type="text" name="name" placeholder="Name" 
                        class="form-control" :class="{ 'is-invalid': form.errors.has('name') }">
                      <has-error :form="form" field="name"></has-error>
                    </div>

                     <div class="form-group">
                      <input v-model="form.email" type="email" name="email" placeholder="Email" 
                        class="form-control" :class="{ 'is-invalid': form.errors.has('email') }">
                      <has-error :form="form" field="email"></has-error>
                    </div>

                    <div class="form-group">
                        <textarea v-model="form.bio" name="bio" id="bio"
                        placeholder="Short bio for user (Optional)"
                        class="form-control" :class="{ 'is-invalid': form.errors.has('bio') }"></textarea>
                        <has-error :form="form" field="bio"></has-error>
                    </div>


                    <div class="form-group">
                        <select name="type" v-model="form.type" id="type" class="form-control" :class="{ 'is-invalid': form.errors.has('type') }">
                            <option value="">Select User Role</option>
                            <option value="admin">Admin</option>
                            <option value="user">Standard User</option>
                            <option value="author">Author</option>
                        </select>
                        <has-error :form="form" field="type"></has-error>
                    </div>

                    <div class="form-group">
                        <input v-model="form.password" type="password" name="password" id="password"
                        class="form-control" :class="{ 'is-invalid': form.errors.has('password') }">
                        <has-error :form="form" field="password"></has-error>
                    </div>


              </div>
              <div class="modal-footer">
                <button type="button" class="btn btn-danger" data-dismiss="modal">Close</button>
                <button v-show="editmode" type="submit" class="btn btn-success">Update</button>
                <button v-show="!editmode" type="submit" class="btn btn-primary">Create</button>
              </div>

              </form>
            </div>
          </div>
        </div>
    </div>
    
</template>

<script>
    export default {
        data(){
            return{
                editmode:false,
                users:{},
                form: new Form({
                    id:'',
                    name:'',
                    email:'',
                    password:'',
                    type:'',
                    bio:'',
                    photo:''
                })
            }
        },

        methods:{
            getResults(page = 1) {
              axios.get('api/user?page=' + page)
                .then(response => {
                  this.users = response.data;
                });
            },
            updateUser(){
                this.form.put('api/user/'+this.form.id)
                .then(()=>{
                    this.$Progress.start();
                    Swal.fire(
                      'Updated!',
                      'Information has been udated.',
                      'success'
                    )
                    Fire.$emit('refreshUser');
                    $('#addNew').modal('hide');
                    this.$Progress.finish();
                })
                .catch(()=>{
                    this.$Progress.fail();
                })
            },
            editModal(user){
                this.editmode=true;
                this.form.reset();
                this.form.clear();
                $('#addNew').modal('show');
                this.form.fill(user);
            },
            newModal(){
                this.editmode=false;
                this.form.reset();
                this.form.clear();
                $('#addNew').modal('show');
            },
            loadUsers(){
                if(this.$gate.isAdmin()){

                axios.get('api/user').then(({data})=>(this.users=data));
                }
            },
            createUser(){
                this.$Progress.start();
                this.form.post('api/user')
                .then(()=>{
                Fire.$emit('refreshUser');
                $('#addNew').modal('hide');
                Toast.fire({
                  icon: 'success',
                  title: 'Signed in successfully'
                })
                this.$Progress.finish();

                })
                .catch(()=>{
                    this.$Progress.fail();
                })
            },
            deleteUser(id){
                Swal.fire({
                  title: 'Are you sure?',
                  text: "You won't be able to revert this!",
                  icon: 'warning',
                  showCancelButton: true,
                  confirmButtonColor: '#3085d6',
                  cancelButtonColor: '#d33',
                  confirmButtonText: 'Yes, delete it!'
                }).then((result) => {

                    if (result.value) {
                        this.form.delete('api/user/'+id).then(()=>{
                            
                            Swal.fire(
                              'Deleted!',
                              'Your file has been deleted.',
                              'success'
                            )
                            Fire.$emit('refreshUser');
                        })
                        .catch(()=>{
                            Swal.fire(
                              'Sorry!',
                              'Your file has not been deleted:)',
                              'error'
                            )
                        })
                    }else{
                        Swal.fire(
                          'Sorry!',
                          'Your file has not been deleted:)',
                          'error'
                        )
                    }

                })
            }
        },

        created() {

          Fire.$on('searching',() => {
                let query = this.$parent.search;
                this.$Progress.start();
                axios.get('api/findUser?q=' + query)
                .then((data) => {
                    this.users = data.data;
                    this.$Progress.finish();
                })
                .catch(() => {

                })
            })
           this.loadUsers();
           Fire.$on('refreshUser',() => {
               this.loadUsers();
           });

            
        }
    }
</script>
